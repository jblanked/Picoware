from micropython import const
from picoware.system.buttons import BUTTON_BACK
from picoware.system.colors import TFT_BLACK
from picoware.system.vector import Vector

SPRITE_WIDTH = const(239)
SPRITE_HEIGHT = const(149)

x: int = 0  # Start logo at
y: int = 0  # top left corner,
vx: int = 1  # moving right
vy: int = 1  # and down

pos: Vector = None
size: Vector = None


def start(view_manager) -> bool:
    """Start the app"""
    global x, y, vx, vy, pos, size

    # Reset position to top-left corner
    x = 0
    y = 0
    vx = 5
    vy = 5
    pos = Vector(x, y)
    size = Vector(SPRITE_WIDTH, SPRITE_HEIGHT)

    return True


def run(view_manager) -> None:
    """Run the app"""
    input_manager = view_manager.input_manager
    input_button = input_manager.button

    if input_button == BUTTON_BACK:
        view_manager.back()
        input_manager.reset()
        return

    global x, y, vx, vy

    draw = view_manager.draw
    draw.fill_screen(TFT_BLACK)
    pos.x = x
    pos.y = y
    draw.image_bytearray_1bit(pos, size, LOGO)
    draw.swap()

    # Update sprite position, bouncing off all 4 sides
    x += vx
    if x <= 0 or x >= draw.size.x - SPRITE_WIDTH:
        vx *= -1
        x = max(0, min(x, draw.size.x - SPRITE_WIDTH))
    y += vy
    if y <= 0 or y >= draw.size.y - SPRITE_HEIGHT:
        vy *= -1
        y = max(0, min(y, draw.size.y - SPRITE_HEIGHT))


def stop(view_manager) -> None:
    """Stop the app"""
    from gc import collect

    global x, y, vx, vy, pos, size

    # Reset position to top-left corner
    x = 0
    y = 0
    vx = 5
    vy = 5
    pos = None
    size = None

    collect()
